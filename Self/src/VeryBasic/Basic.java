package VeryBasic;

public class Basic {
	
	public static void main(String[] args) {
		
		// primitive 
		
		
		//numeric 
		//integer
		byte a  = 20  ; // 1 byte 
		short b  = 90 ; //  2 byte 
		int c  = 90 ; //  4 byte 
		long d  = 90 ;  // 8 byte 
		
		//decimal
		float e  = 5.3f ;// 4 byte
		double f  = 4.5 ; // 8 byte 
		
		
		
		
		
		
		/// non numeric

		char g  = 'c'; //2 byte 
		boolean h  = true ; // 1 bit
		
		
		// non - primitive
		
		//class 
		//interface 
		//Arrays
		
		int [] arr  = new int [3];
		
		Basic [] newArr = new Basic[3] ;
		
		arr[0] = 1;
		
		
		
		
		
		
		
	}

}
